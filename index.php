                  <div class="panel-heading">
                      <p class="panel-title" style="color: #964f8e">
                        <a data-toggle="collapse" data-parent="#accordion3" href="#why_name">Why the name <em>Eldur Heron Farm</em>?</a>
                    </p>
                  </div>
                  <div id="why_name" class="panel-collapse collapse">
                      <div class="panel-body">
                          <p>The great blue heron is an iconic symbol of this area's unique beauty, and it just so happens to be one of my all-time favorite animals. Since there is already a farm locally using the <em>Blue Heron</em> farm name, and I wanted to tie my own uniqueness into the farm's name, I decided upon using the Icelandic word for <em>fire</em> = <em>eldur</em> to both allude to my fiery red hair, and to include a reference to my family's Icelandic heritage.</p>
                      </div>
                  </div>
                </div>
                
                <div class="panel panel-default" style="border-color: #964f8e;">
                          <div class="panel-heading">
                              <p class="panel-title" style="color: #964f8e">
                                <a data-toggle="collapse" data-parent="#accordion3" href="#why_rainbow">Why the rainbow colors?</a>
                              </p>
                        </div>
                  <div id="why_rainbow" class="panel-collapse collapse">
                      <div class="panel-body">
                          <p>Growing a diverse mix of vegetables is only just the beginning of all the diversity that <em>Eldur Heron Farm</em> intends to foster, support, and protect. Helping foster the next generation of farmers through education and knowledge sharing. Helping support more than one local market with fresh, local, organic produce (see <a href="#support">Support</a> section). Helping protect a diverse offering of organic, open-pollinated, heirloom seeds for gardeners and growers around the globe.</p>
                          <p>Using the colors of the rainbow to help symbolize various aspects of the layers of diversity utilized by <em>Eldur Heron Farm</em> is a simple way to visualize both the differences that separate the various crops or practices, and all the cycles or patterns found throughout nature. Initially, the idea to use these colors <abbr title="Red Orange Yellow Green Blue Violet">ROYGBV</abbr> stemmed from my experience with the visible light spectrum, which is required for plant growth (along with soil, water, and air). Below are the wavelengths of light for each of the colors in  <abbr title="Red Orange Yellow Green Blue Violet">ROYGBV</abbr>, ordered from longest to shortest wavelength, in nanometers (nm):</p>
                          <ol>
                              <li style="color: #bd1e24">Red 620-750 nm</li>
                              <li style="color: #e97600">Orange 590-620 nm</li>
                              <li style="color: #e6bb00">Yellow 570-590 nm</li>
                              <li style="color: #007256">Green 495-570 nm</li>
                              <li style="color: #0067a7">Blue 450-495 nm</li>
                              <li style="color: #964f8e">Violet 380-450 nm</li>
                          </ol>
                          <p>Additionally, <em>Eldur Heron Farm</em> whole-heartedly supports this seemingly outlandish idea that all humans should be treated with equality, regardless of how one identifies. <em>Eldur Heron Farm</em> is proud to support those with pride in who they are. Hopefully, anyone reading this far into the <em>About</em> section knows that these colors were also chosen as a symbol of this support as well. <em>Eldur Heron Farm</em> will always be a safe place for you to be you.</p>
